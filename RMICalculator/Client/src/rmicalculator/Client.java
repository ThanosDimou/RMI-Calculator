    public void setNumbers() {
        // set values in x, y variables from txtDisplay
        if (x == 0) {
            x = Integer.parseInt(txtDisplay.getText());
        } else {
            y = Integer.parseInt(txtDisplay.getText());
        }
    }

    /**
     *
     * @param buttons
     */
    public void writeNumbers(String buttons) {
        //write chars to text field and 
        //allow to write more than one char in txtField
        //but first clear the field
        if (param == null) {
            reset();
            String all = txtDisplay.getText() + buttons;
            txtDisplay.setText(all);
        } else {
            String all = txtDisplay.getText() + buttons;
            txtDisplay.setText(all);
        }
    }

