    private void txtDisplayKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDisplayKeyPressed
        // keystrokes for efficient
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_CONTROL: //give result
                apotelesma();
                break;
            case KeyEvent.VK_ADD: //for numpad only because               
                plus();           // shift +  its not efficient
                break;
            case KeyEvent.VK_MINUS: // only on main keyboard
                sub();              //for a reason numpad minus not responding
                break;
            case KeyEvent.VK_MULTIPLY:  //for numpad only because   
                multiply();             // shift *  its not efficient
                break;
            case KeyEvent.VK_DIVIDE:  //for numpad
                divide();
                break;
            case KeyEvent.VK_SLASH: //for keyboard
                divide();
                break;
            default:
                break;
        }


    }//GEN-LAST:event_txtDisplayKeyPressed

