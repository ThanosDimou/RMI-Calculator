    private void txtDisplayKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDisplayKeyTyped
        //not permit to write non digit characters
        //and reset txtfield if not any parametr is active
        char c = evt.getKeyChar();
        if (param == null) {
            reset();
            if (!Character.isDigit(c) || (c == KeyEvent.VK_BACKSPACE) || (c == KeyEvent.VK_DELETE)) {
                evt.consume();
            }
        } else if (!Character.isDigit(c) || (c == KeyEvent.VK_BACKSPACE) || c == KeyEvent.VK_DELETE) {
            evt.consume();
        }
    }//GEN-LAST:event_txtDisplayKeyTyped

